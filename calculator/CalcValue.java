
/**
 * @author Adelina
 * 
 * perform calculation between two variables 
 *
 */
public class CalcValue {
	
	private String firstVar;
	private String secondVar;
	
	public CalcValue(String firstVar, String secondVar) {
		this.firstVar = firstVar;
		this.secondVar = secondVar;
	}

	public String getFirstVar() {
		return firstVar;
	}
	
	public String getSecondVar() {
		return secondVar;
	}

}
